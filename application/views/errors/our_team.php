			<p>We hope our users will be happy with our web site and we will work hard to earn your business. Unfortunately, the inevitable sometimes happens when artist for some reason are not happy with our product. We hope during your test drive, “1–month free,” you remain a loyal customer. However, if you must cancel we will be happy to oblige within the following guidelines.</p>
			<h3>Refund Guidelines:</h3>
			<p>SHP Inc. provides users with a 1-month free trial for the Absolute &#038; Music plans.</p>
			<ol>
				<li>Once the trial period is over no refunds will be extended.</li>
				<li>During the trial period customers may cancel for any reason.</li>
				<li>Refunds will be prorated and calculated by a daily charge.</li>
			</ol>
			<p>SHP will NOT provide notice the trial period is getting ready to expire. </p>
			<ol>
				<li>Again, it’s up to the customer to cancel within the trial period.</li>
				<li>Once a customer notifies SHP a cancellation number with a confirmation email showing your subscription has been canceled. </li>
				<li>Please reference this number when contacting customer support.</li>
			</ol>
