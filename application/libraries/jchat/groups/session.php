	function get_user($identifier)
	{
		if($identifier == "id" || $identifier == "ID")
		{
			return $_SESSION['jChat_id'];;
		}
		
		if($identifier == "username" || $identifier == "USERNAME" || $identifier == "Username")
		{
			return $_SESSION['jChat_username'];
		}
		
	}
	
